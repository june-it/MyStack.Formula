namespace MyStack.Formula.FormulaNodes.Operator
{
    /// <summary>
    /// 表示操作符类型
    /// </summary>
    public enum OperatorType
    {
        /// <summary>
        /// 表示加号 '+'
        /// </summary>
        Plus,
        /// <summary>
        /// 表示减号 '-'
        /// </summary>
        Minus,
        /// <summary>
        /// 表示乘号 '*'
        /// </summary>
        Multiply,
        /// <summary>
        /// 表示除号 '/'
        /// </summary>
        Divide,
        /// <summary>
        /// 表示幂 '^'
        /// </summary>
        Pow,
        /// <summary>
        /// 表示正弦（sine） 'SIN'
        /// </summary>
        Sin,
        /// <summary>
        /// 表示余弦（cosine） 'COS'
        /// </summary>
        Cos,
        /// <summary>
        /// 表示正切（tangent） 'TAN'
        /// </summary>
        Tan,
        /// <summary>
        /// 表示余切（cotangent） 'COT'
        /// </summary>
        Cot,
       
    }
}
